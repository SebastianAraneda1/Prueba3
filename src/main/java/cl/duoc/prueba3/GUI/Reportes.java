    private void comboClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboClienteActionPerformed
        // TODO add your handling code here:
        areaVentas.setText("");
        String cliente = String.valueOf(comboCliente.getSelectedItem());
        StringBuilder sb = new StringBuilder();
        for(Venta venta : listVenta.listar()){
            if(cliente.toLowerCase() == venta.cliente.toLowerCase()){
                sb.append(venta.cliente+ " compro "+ venta.cantidad + " de "+ venta.producto+ " del vendedor "
                + venta.vendedor + " el "+venta.fechaVenta+ " para entregar el "+venta.fechaEntrega+ 
                " con un total de: $"+venta.totalPago+"\n");
            }
        }
        areaVentas.setText(String.valueOf(sb));
    }//GEN-LAST:event_comboClienteActionPerformed


